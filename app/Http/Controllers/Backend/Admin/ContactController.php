    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $data = Contact::find($id);
        return view('admin.contact.edit',compact('data'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $data = Contact::find($id);
        $data->email= $request->email;
        $data->phone = $request->phone;
        $data->address= $request->address;
        $data->content= $request->content;
        $data->opening_hour = $request->opening_hour;
        $data->facebook=$request->facebook;
        $data->twitter=$request->twitter;
        $data->linkedin=$request->linkedin;
        $data->instagram=$request->instagram;
        $data->map=$request->map;
        if($request->hasfile('contact_banner_image'))
        {
            @unlink(public_path('uploads/images/'.$data->contact_banner_image));
            $file = $request->file('contact_banner_image');
            $filename = uniqid() . '.' . $file->getClientOriginalExtension($file);
            $file->move(public_path('uploads/images'),$filename);
            $data->contact_banner_image = $filename;
        }
        $data->save();
        return back()->with('flash_success', 'Updated successfully');

    }



